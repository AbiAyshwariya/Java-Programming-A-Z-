import java.lang.*;

class Assignment{
	public static void main(String args[]){
		int a=123;
		System.out.println("Value of a: "+a);
		a+=10;
		System.out.println("Addition a+=10:"+a);
		a-=10;
		System.out.println("Subtraction a-=10:"+a);
		a*=10;
		System.out.println("Multiplication a*=10:"+a);
		a/=10;
		System.out.println("Division a/=10:"+a);
		a%=10;
		System.out.println("Modulus a%=10:"+a);
	}
}