import java.lang.*;

class TypeCasting{
	public static void main(String args[]){
		int a=10;
		double b=a;
		double d=25.5392;
		int c=(int)d;
		System.out.println("Int a: "+a);
		System.out.println("Double b: "+b);
		System.out.println("Int c: "+c);
		System.out.println("Double d: "+d);
	}
}